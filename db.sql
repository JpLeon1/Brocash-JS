CREATE DATABASE brocash;
USE brocash;

CREATE TABLE REGISTRO_USUARIO(
ID_USUARIO INT PRIMARY KEY,
NOMBRE VARCHAR(30),
APELLIDO VARCHAR(20),
EDAD INT,
EMAIL VARCHAR(255),
TELEFONO VARCHAR(15),
PASSWORD VARCHAR(255)
);

CREATE TABLE ANALISTA(
ID_ANALISTA INT PRIMARY KEY,
NOMBRE VARCHAR(30)
);

CREATE TABLE CUENTA(
ID_CUENTA VARCHAR(40) PRIMARY KEY,
ID_USUARIO INT,
TIPO_CUENTA VARCHAR(80),
SALDO DECIMAL(12,2),
FOREIGN KEY (ID_USUARIO) REFERENCES REGISTRO_USUARIO(ID_USUARIO)
);

CREATE TABLE CREDITO(
ID_CREDITO INT PRIMARY KEY,
ID_USUARIO INT,
ID_ANALISTA INT,
INGRESOS DECIMAL(10,2),
ESTADO VARCHAR(40),
FOREIGN KEY (ID_USUARIO) REFERENCES REGISTRO_USUARIO(ID_USUARIO),
FOREIGN KEY (ID_ANALISTA) REFERENCES ANALISTA(ID_ANALISTA)
);

CREATE TABLE PAGO(
ID_PAGO INT PRIMARY KEY,
ID_CREDITO INT,
FECHA DATE,
MONTO DECIMAL(15,2),
METODO VARCHAR(40),
FOREIGN KEY (ID_CREDITO) REFERENCES CREDITO(ID_CREDITO)
);

CREATE TABLE TRANSACCION(
ID_TRANSACCION INT PRIMARY KEY,
ID_CUENTA VARCHAR(40),
FECHA DATE,
MONTO DECIMAL(15,2),
FOREIGN KEY (ID_CUENTA) REFERENCES CUENTA(ID_CUENTA)
);


